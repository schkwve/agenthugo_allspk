file_prefix = [
	"Czh [", # Czech
	""
	]
file_suffix = "].wav"

lang = 0 # Czech by default

not_found = []
not_copied = []

def copy_files(name, array):
	for n in array:
		filename = file_prefix[lang] + n + file_suffix
		target_filename = name + "/" + filename
		try:
			os.rename(filename, target_filename)
		except FileNotFoundError:
			not_found.append(filename)
			continue
		except OSError as error:
			not_copied.append(filename)
			continue

def main():
	cwd = os.getcwd() + "/"

	# Create directory structure
	print("[*] Creating directory structure...")
	for dir in output_dirnames:
		try:
			os.mkdir(cwd + dir, 0o777)
		except:
			# Silently skip existing directories
			continue
	
	print("[*] Moving files...")
	copy_files("alltoobright", alltoobright)
	copy_files("agent_two", agent_two)
	copy_files("enemy_generic", enemy_generic)
	copy_files("graffenphennich", graffenphennich)
	copy_files("hugo", hugo)
	copy_files("informant", informant)
	copy_files("kizzy_mi", kizzy_mi)
	copy_files("krivak", krivak)
	copy_files("max_schmertz", max_schmertz)
	copy_files("nogo", nogo)
	copy_files("pallazo", pallazo)
	copy_files("roztomila", roztomila)
	copy_files("russian", russian)
	copy_files("unknown", unknown)
	copy_files("w", w)

	if not_found:
		print("[!] Following files were not found: ", end="")
		for file in not_found:
			print(file + ", ", end="")
		print("")

	if not_copied:
		print("[!] Following files could not be copied: ", end="")
		for file in not_copied:
			print(file + ", ", end="")
		print("")
	
	print("[ ] Done!")

main()